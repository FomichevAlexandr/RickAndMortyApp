import UIKit

extension UIColor
{
    
    open class var mainBackgroundColor: UIColor {
        return UIColor(red: 0.793, green: 0.913, blue: 0.924, alpha: 1)
    }
    
    open class var customPinkColor: UIColor {
        return UIColor(red: 0.933, green: 0.732, blue: 0.833, alpha: 1)
    }
    
    open class var customBlueColor: UIColor {
        return UIColor(red: 0.682, green: 0.851, blue: 0.859, alpha: 1)
    }
    
}
